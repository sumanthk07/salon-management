            <h2>3.3 ER Diagram</h2>
                <p>An entity-relationship model (ER model) is a data model for describing the data
                    or information aspects of a business domain or its process requirements, in an abstract
                    way that lends itself to ultimately begin implemented in the database such as a relational
                    database. The main components of ER model are entities (things) and the relationship
                    that can exist among them. Entity â€“ relationship modeling was developed by Peter chen
                    and published in 1976 paper. The ER diagram is drawn to have a better understanding
                    of the whole scenario, it is used to conceptualize the phenomena, actions and
                    interactions between various entities and to arrive at the specific requirements in a
                    comprehensive manner. An entity relation relationship model is the result of using a
                    systematic process to describe and define a subject area of business data. The data is
                    represented as components (entities) that are linked with each other by relationships
                    that express the dependencies and requirements between them, such as: one building
                    may be divided into number of apartments, but one apartment can be located in one
                    building. Entities may have various properties (attributes) that characterize them.
                    Diagrams created to represent these entities, attributes, and relationships graphically are
                    called entity-relationship diagrams. An ER model is typically implemented as database.
                    In the case of a relational database, which stores data in tables, every row of each table
                    represents one instance of an entity. Some data fields in these tables to indexes in other
                    tables; such pointers are physical implementation of the relationships. The three-
                    schema approach to software engineering uses three levels of ER models may be
                    developed. </p>
